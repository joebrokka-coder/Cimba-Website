    description:
      "Interoperability allows external agents and tools to interact with Cimba. Cimba provides an MCP server that exposes agents, workflows, and data connections through a structured interface. This allows external AI systems and tools to discover, control, and integrate with Cimba workflows. As a result, organizations can connect Cimba into broader AI ecosystems and automate workflows across multiple systems.",
    icon: Server,
