    title: "Workflows",
    description:
      "Step-by-step analytical processes generated by the agent to resolve a request. When an agent receives a question or executes a playbook, it generates a workflow to carry out the required analysis. These workflows break complex problems into sequential steps that can query data, perform calculations, and produce outputs. The result is a transparent process that shows how the agent arrived at its answer.",
